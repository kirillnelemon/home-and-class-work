import random


class Number:
    # –ó–∞–º–µ–Ω–∏–ª–∏ init –Ω–∞ –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π –º–µ—Ç–æ–¥ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ __init__
    def __init__(self, size):
        # –°–æ–∑–¥–∞–µ–º —Å–ø–∏—Å–æ–∫ —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª —Å–ª—É—á–∞–π–Ω—ã–º –æ–±—Ä–∞–∑–æ–º (–æ—Ç -10 –¥–æ 10 –¥–ª—è —Ä–∞–∑–Ω–æ–æ–±—Ä–∞–∑–∏—è)
        self.numbers = [random.randint(-10, 10) for _ in range(size)]
        print(f"–ò—Å—Ö–æ–¥–Ω—ã–π —Å–ø–∏—Å–æ–∫: {self.numbers}")

    def process_list(self):
        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –ø—É—Å—Ç–æ–π —Å–ø–∏—Å–æ–∫ –ø–µ—Ä–µ–¥ –≤—ã—á–∏—Å–ª–µ–Ω–∏–µ–º —Å—Ä–µ–¥–Ω–µ–≥–æ
        if not self.numbers:
            print("–°–ø–∏—Å–æ–∫ –ø—É—Å—Ç.")
            return []

        # –í—ã—á–∏—Å–ª—è–µ–º —Å—Ä–µ–¥–Ω–µ–µ –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–æ–µ –≤—Å–µ—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤
        avg = sum(self.numbers) / len(self.numbers)
        print(f"–°—Ä–µ–¥–Ω–µ–µ –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–æ–µ: {avg}")
        list_len = len(self.numbers)

        # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –≥—Ä–∞–Ω–∏—Ü—ã –¥–ª—è –¥–≤—É—Ö —Ç—Ä–µ—Ç–µ–π –∏ –æ–¥–Ω–æ–π —Ç—Ä–µ—Ç–∏
        two_thirds_index = (list_len * 2) // 3
        one_third_index = list_len // 3

        # –ü–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —á–∞—Å—Ç–µ–π —Å–ø–∏—Å–∫–∞ –ø–æ—Å–ª–µ –æ–±—Ä–∞–±–æ—Ç–∫–∏
        part1 = []
        part2 = []

        if avg > 0:
            # –†–∞–∑–¥–µ–ª—è–µ–º —Å–ø–∏—Å–æ–∫ –Ω–∞ —á–∞—Å—Ç–∏ –¥–ª—è —Å–ª—É—á–∞—è avg > 0
            part1 = self.numbers[:two_thirds_index]
            part2 = self.numbers[two_thirds_index:]
            # –°–æ—Ä—Ç–∏—Ä—É–µ–º –ø–µ—Ä–≤—ã–µ –¥–≤–µ —Ç—Ä–µ—Ç–∏ –ø–æ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—é
            part1.sort()
            print("–°—Ä–µ–¥–Ω–µ–µ > 0: –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω—ã –ø–µ—Ä–≤—ã–µ 2/3.")
        else:
            # –†–∞–∑–¥–µ–ª—è–µ–º —Å–ø–∏—Å–æ–∫ –Ω–∞ —á–∞—Å—Ç–∏ –¥–ª—è —Å–ª—É—á–∞—è avg <= 0
            part1 = self.numbers[:one_third_index]
            part2 = self.numbers[one_third_index:]
            # –°–æ—Ä—Ç–∏—Ä—É–µ–º —Ç–æ–ª—å–∫–æ –ø–µ—Ä–≤—É—é —Ç—Ä–µ—Ç—å (–µ—Å–ª–∏ —Å—Ä–µ–¥–Ω–µ–µ <= 0)
            part1.sort()
            print("–°—Ä–µ–¥–Ω–µ–µ <= 0: –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–∞ —Ç–æ–ª—å–∫–æ –ø–µ—Ä–≤–∞—è 1/3.")

        # –û—Å—Ç–∞–ª—å–Ω—É—é —á–∞—Å—Ç—å —Å–ø–∏—Å–∫–∞ —Ä–∞—Å–ø–æ–ª–∞–≥–∞–µ–º –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ
        part2.reverse()

        # –û–±—ä–µ–¥–∏–Ω—è–µ–º —á–∞—Å—Ç–∏ –æ–±—Ä–∞—Ç–Ω–æ
        self.numbers = part1 + part2
        return self.numbers


